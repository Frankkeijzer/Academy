package oefeningen.OCA2.Obj4;

public class arrayOpEenLijn {
	public static void main(String[] args) {
		int[] a, b, c;
		// allemaal 1-dimensie
		c = new int[] {1,3};
		
		int aa[], bb[][], cc[];
		// mutli- en één dimensionaal door elkaar, wel hetzelfde type
		
		aa = new int[] {4,6};
		bb = new int[][] {{4,5},{3,5}};
		cc = new int[] {2,4};
		
		// let op bij 1 lijn definieren
		int[] aaa[], bbb, ccc[][];
		aaa =  new int [][] {{4,5},{6,7}};
		bbb = new int[] {2,9};
		ccc = new int [][][] {{{5,9}},{{3,5},{11,12}}};
		
 	}
}
